//Write a for loop that will loop through every character of a word and print out each character, each on a separate line
public class T14R {
    public static void main(String[] args) {
    String str="wow";
    for (int i =0; i<str.length();i ++){// increment every word
        System.out.println(str.charAt(i));// method to access the character at the current index i,
    }
        System.out.println();
    }
}













